package vo;

public class MemberVO {
	private String id;
	private String pw;
	private String name;
	private String regno;

	public MemberVO(String id, String pw, String name, String regno) {
		this.id = id;
		this.pw = pw;
		this.name = name;
		this.regno = regno;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRegno() {
		return regno;
	}

	public void setRegno(String regno) {
		this.regno = regno;
	}

	@Override
	public String toString() {
		return "MemberVO [id=" + id + ", pw=" + pw + ", name=" + name + ", regno=" + regno + "]";
	}
	
	
}
